#include "3-calc.h"

/**
 * op_add - function that sums up two numbers
 *
 * @a: This is the number 1
 * @b: This is the number 2
 *
 * Return: The result of the sum of the two numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -  funtion that subtract two numbers
 *
 * @a: this is the number 1
 * @b: this is the number 2
 *
 * Return: The result of the sub numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that multiply two numbers
 *
 * @a: this is the number 1
 * @b: This is the number 2
 *
 * Return: The result of the multiplication of two numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -  Function divides two numbers
 *
 * @a: This is the number 1
 * @b: this is the number 2
 *
 * Return: the results of the division of two numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Functions modulation of two numbers
 *
 * @a: this is the number 1
 * @b: this is the number 2
 *
 * Return: The result of the modulation of two numbers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
